package exam04;

public class Student {
	
	String name;
	int Score;
	
	public Student(String name, int score) {
		super();
		this.name = name;
		Score = score;
	}
	@Override
	public String toString() {
		return "Student [name=" + name + ", Score=" + Score + "]";
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
